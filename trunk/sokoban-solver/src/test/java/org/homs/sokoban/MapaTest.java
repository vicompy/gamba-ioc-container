package org.homs.sokoban;

import junit.framework.Assert;

import org.junit.Test;

public class MapaTest {

	@Test
	public void test1() {
		final String l = "" +
		"########\n" +
		"#      #\n" +
		"#  @$*  #\n" +
		"#  $..  #\n" +
		"#      #\n" +
		"#### ###\n" +
		"#      #\n" +
		"# #### #\n" +
		"# #  # #\n" +
		"#   ## #\n" +
		"# ## # #\n" +
		"# #    #\n" +
		"########\n" +
		"\n";

//		System.out.println(new Mapa(l).toString2());

		Assert.assertEquals(
			"13,9,3<" +
			"   |<" +
			"#########<" +
			"#      ##<" +
			"#  @$*  #-<" +
			"#  $..  #<" +
			"#      ##<" +
			"#### ####<" +
			"#      ##<" +
			"# #### ##<" +
			"# #  # ##<" +
			"#   ## ##<" +
			"# ## # ##<" +
			"# #    ##<" +
			"#########<" +
			"<" +
			"#########<" +
			"#      ##<" +
			"#   ##  #<" +
			"#  #    #<" +
			"#      ##<" +
			"#### ####<" +
			"#      ##<" +
			"# #### ##<" +
			"# #  # ##<" +
			"#   ## ##<" +
			"# ## # ##<" +
			"# #    ##<" +
			"#########<" +
			"<",
			new Mapa(l).toString2().replace('\n', '<'));
	}


	@Test
	public void test2() {
		final String l = "" +
		"##############\n" +
		"# *          #\n" +
		"# ############\n" +
		"#@.          #\n" +
		"# ############\n" +
		"# $          #\n" +
		"##############\n" +
		"\n";

//		System.out.println(new Mapa(l).toString2());

		Assert.assertEquals(
			"7,14,2<" +
			" |<" +
			"##############<" +
			"# *          #<" +
			"# ############<" +
			"#@.          #-<" +
			"# ############<" +
			"# $          #<" +
			"##############<" +
			"<" +
			"##############<" +
			"# ############<" +
			"# ############<" +
			"#            #<" +
			"# ############<" +
			"# ############<" +
			"##############<" +
			"<",
			new Mapa(l).toString2().replace('\n', '<'));
	}

}


